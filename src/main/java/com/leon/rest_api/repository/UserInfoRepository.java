package com.leon.rest_api.repository;


import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import com.leon.rest_api.entities.UserInfo;

import java.math.BigDecimal;
import java.util.Optional;

@Repository
public interface UserInfoRepository extends JpaRepository<UserInfo, BigDecimal> {

    Optional<UserInfo> findByUserId(BigDecimal userId);
}
